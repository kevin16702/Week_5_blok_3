Persona 5 is een Japanse Role Playing game uit de shin megami tensei series. Het is gemaakt door atlus en uitgebracht op 15 september 2016. Het is een singleplayer game met social simulation aspecten. De director is Katsura Hashino en de publishers waren in Japan: Atlus, voor noord Amerika: Atlus USA, voor Europa: Deep Silber. De artiesten waren Shigenori Soejima en Masayoshi Suto, de schrijvers waren Yuichiro Tanaka, Shinji Yamamoto en Katsura Hashino, de composer was Shoji Meguro en de main programmer was Yujio Kosaka. 
Persona 5, net zoals andere voorafgaande games, is een role playing game met levens simulatie elementen. De game volgt het leven van de main character, die zijn dagelijks balanceert tussen het naar school gaan, relaties bouwen met andere mensen en vechten met creaties genaamt schaduwen in het mysterieuze mementos. Ook gaat de groep, genaamt “phantom thieves” in de speciale dimensie naar palijzen om harten te veranderen van mensen die slechte bedoelingen hebben. De speler kan zelf kiezen hoe hij zijn dag indeelt en meeste activiteiten nemen een tijdsdeel in beslag.
Een van de terugkerende concepten is de velvet room. De Velvet room is een plek waar je je persona’s kan fuseren en met Igor kan praten. De velvet room komt in elke game weer terug.

